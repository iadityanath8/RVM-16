from enum import Enum

class Inst(Enum):
    MOV_REG = 0x01
    MOV_IMM = 0x02

    LOAD = 0x03
    STORE_REG = 0x04
    STORE_IMM = 0x05
    
    ADD_REG = 0x06
    ADD_IMM = 0x07
    SUB_REG = 0x08
    SUB_IMM = 0x09
    MUL_REG = 0x0A
    MUL_IMM = 0x0B
    DIV_REG = 0x0C
    DIV_IMM = 0x0D
    AND_REG = 0x0E
    AND_IMM = 0x0F
    OR_REG  = 0x10
    OR_IMM  = 0x11

    CMP_REG = 0x12
    CMP_IMM = 0x13

    INC     = 0x14
    DEC     = 0x15

    PUSH_REG = 0x16
    PUSH_IMM = 0x17
    POP      = 0x18

    CALL     = 0x19
    RET      = 0x1A

    JMP      = 0x1B
    JG       = 0x1C
    JGE      = 0x1D
    JE       = 0x1E
    JA       = 0x1F
    JAE      = 0x20

    HLT      = 0x21


class Reg(Enum):
    R1 = 0x00
    R2 = 0x01
    R3 = 0x02
    R4 = 0x03
    R5 = 0x04
    R6 = 0x05
    R7 = 0x06
    R8 = 0x07

    PC = 0x08
    SP = 0x09
    FP = 0x0A
